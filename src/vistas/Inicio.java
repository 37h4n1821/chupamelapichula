    private void Actualizar(){
        
        DefaultTableModel DT=new DefaultTableModel();
        DT.addColumn("nombre");
        DT.addColumn("rut");
        DT.addColumn("Fecha_Inicio");
        DT.addColumn("cap_suc");
        DT.addColumn("penes_chupados");
        

        Object[] fila=new Object[5];
        for (Succionador suc:Succionadores) { 
            fila[0]=suc.getNombre();
            fila[1]=suc.getRut();
            fila[2]=suc.getFecha_Inicio();
            fila[3]=suc.getCap_suc();
            fila[4]=suc.getPenes_chupados();
            DT.addRow(fila);
        }

        jTable1.setModel(DT);
        jTable1.setAutoCreateRowSorter(false);
        
        
        //Estupidez:
        ArrayList<Color> colores= new ArrayList<Color>();
        colores.add(Color.red);
        colores.add(Color.blue);
        colores.add(Color.green);
        colores.add(Color.black);
        colores.add(Color.white);
        colores.add(Color.CYAN);
        colores.add(Color.ORANGE);
        colores.add(Color.yellow);
        colores.add(Color.MAGENTA);
        jLabel1.setForeground(colores.get(new Random().nextInt(colores.size())));
        
    }

