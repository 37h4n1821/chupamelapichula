    private void EditarAmanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditarAmanteMouseClicked
        // TODO add your handling code here:
        
        System.out.println(jTable1.getSelectedRow());
        if(jTable1.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "[Error al editar]\nSeleccione en la tabla el registro a editar", "Sin selección", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                new SuccionadorVista(Succionadores,Succionadores.get(jTable1.getSelectedRow())).setVisible(true);
            }
        });
    }//GEN-LAST:event_EditarAmanteMouseClicked

    private void EliminarAmanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EliminarAmanteMouseClicked
        // TODO add your handling code here:
         System.out.println(jTable1.getSelectedRow());
        if(jTable1.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "[Error al eliminar]\nSeleccione en la tabla el registro a eliminar", "Sin selección", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Succionadores.remove(jTable1.getSelectedRow());
        JOptionPane.showMessageDialog(null, "[Eliminador Válido]", "Eliminado correctamente", JOptionPane.DEFAULT_OPTION);
        Actualizar();
    }//GEN-LAST:event_EliminarAmanteMouseClicked

